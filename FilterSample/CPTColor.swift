import UIKit

class CPTColor: NSObject {
    class func whiteColor() -> UIColor {
        return UIColor.whiteColor()
    }
    class func yellowColor() -> UIColor {
        return UIColor(hex: "#ffe33e")
    }
    class func blackColor() -> UIColor {
        return UIColor(hex: "#212121")
    }
}
